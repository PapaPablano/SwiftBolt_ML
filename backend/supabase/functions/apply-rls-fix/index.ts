// One-time function to apply RLS policy fix
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2";

serve(async (req) => {
  try {
    const supabaseClient = createClient(
      Deno.env.get("SUPABASE_URL") ?? "",
      Deno.env.get("SUPABASE_SERVICE_ROLE_KEY") ?? ""
    );

    // Execute the SQL
    const { data, error } = await supabaseClient.rpc('exec_sql', {
      sql: `
        DROP POLICY IF EXISTS "options_ranks_select_authenticated" ON options_ranks;
        CREATE POLICY "options_ranks_select_anon" ON options_ranks
          FOR SELECT TO anon, authenticated
          USING (true);
      `
    });

    if (error) {
      return new Response(JSON.stringify({ error: error.message }), {
        status: 500,
        headers: { "Content-Type": "application/json" },
      });
    }

    return new Response(JSON.stringify({ success: true, message: "RLS policy updated" }), {
      status: 200,
      headers: { "Content-Type": "application/json" },
    });
  } catch (err) {
    return new Response(JSON.stringify({ error: String(err) }), {
      status: 500,
      headers: { "Content-Type": "application/json" },
    });
  }
});
